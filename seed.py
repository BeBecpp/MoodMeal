def seed():
    with app.app_context():
        db.create_all()
        RecipeIngredient.query.delete()
        Recipe.query.delete()
        Ingredient.query.delete()
        db.session.commit()

        for row in INGREDIENTS:
            db.session.add(
                Ingredient(
                    name=row[0],
                    name_en=row[1],
                    category=row[2],
                    taste_profile=row[3],
                    nutrition_note=row[4],
                    common_pairings=row[5],
                    is_common_home_item=row[6],
                )
            )
        db.session.commit()

        for r in RECIPES:
            rec = Recipe(
                title=r["title"],
                description=r["description"],
                meal_time=r["meal_time"],
                mood_tags=r["mood_tags"],
                weather_tags=r["weather_tags"],
                food_style=r["food_style"],
                base_servings=r["base_servings"],
                cook_time_minutes=r["cook_time_minutes"],
                difficulty=r["difficulty"],
                child_friendly=r["child_friendly"],
                instructions=r["instructions"],
                source="local",
            )
            db.session.add(rec)
            db.session.flush()
            for name, amount, unit, sub in r["ingredients"]:
                db.session.add(
                    RecipeIngredient(
                        recipe_id=rec.id,
                        ingredient_name=name,
                        amount=amount or None,
                        unit=unit or None,
                        is_required=True,
                        substitute_options=sub,
                    )
                )
        db.session.commit()
        print(f"Seeded {len(INGREDIENTS)} ingredients and {len(RECIPES)} recipes.")


if __name__ == "__main__":
    seed()
